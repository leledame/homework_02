public class Task007 {
    public static void main(String[] args) {
        System.out.println("Задание 007:");
        float a = 3;  // float для "%" и для "/"
        float b = 4;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("b - a = " + (b - a));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("b / a = " + (b / a));
        System.out.println("b % a = " + (b % a));

    }
}